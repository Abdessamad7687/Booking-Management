    private void seConnecterButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_seConnecterButtonActionPerformed
        // TODO add your handling code here:
        
        String usernameInput = usernameTxt.getText();
        String passwordInput = passwordTxt.getText();
        
        Utilisateur foundUser = null;
        for( Utilisateur usr : us.findAll()){
            if(usernameInput.equals(usr.getUsername())){
                foundUser = usr;
                break;
            }
        }
        
        if(foundUser != null){
            if(passwordInput.equals(foundUser.getPassword())){
                Main main = new Main();
                main.setVisible(true);
                this.setVisible(false);
            } else{
                JOptionPane.showMessageDialog(this, "incorrect password", "Error!", JOptionPane.ERROR_MESSAGE);
            }
        } else{
            JOptionPane.showMessageDialog(this, "User not found", "Error!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_seConnecterButtonActionPerformed

